def main():
# Dictionaries are key value pair
    # | Key    |   Value    |
    # | apple  |   green    |
    fruits ={
        "apple":"green",        
        "orange":"orange",
        "banana":"yellow",
        True:7
    }

     # Accessing with key
    print( fruits["apple"])
    print( fruits["orange"])
    print( fruits["banana"])
    print( fruits[True])


main()